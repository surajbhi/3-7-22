package dataType;

public class VariableDataType {
	public static void main(String[] args) {
		boolean a = true;
		byte b = -120;
		short c = 32767;
		int d = 123456;
		long e = 12345678910l;
		float f = 12.34f;

		System.out.println(b + c + d + e);
		System.out.println("value of byte is" + e + "value of short is" + f);
		System.out.println("values are " + b + c + d + e);

	}

}
