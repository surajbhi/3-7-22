public class TryFinally 
{
	public static void main(String[] args) {
		try
		{
			int a = 10;
			int b = 0;
			int c = a/b;
		}
		finally
		{
			System.out.println("Execution completed");
		}
		System.out.println("ngaha");
		System.out.println("ngaha");
		System.out.println("ngaha");
		System.out.println("ngaha");
		System.out.println("ngaha");
	}
}

